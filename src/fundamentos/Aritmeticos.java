package fundamentos;

public class Aritmeticos {
    public static void aritmetico(String[] args) {
        System.out.println(2 + 3);
        double x = 34.56;
        double y = 2.2;
        
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x / y);
        System.out.println(x * y);
        
        int i = 8;
        int j = 3;
        System.out.println(i + j);
        System.out.println(i - j);
        System.out.println(i / j);
        System.out.println((double)i / j);
        System.out.println(i * j);
        System.out.println(8 % 3);
    }
    
}
